public class NarrowConst {
    public static void main(String[] args) {
        byte a = (byte) 300;
        System.out.println(a);
        short b = (short) 70000;
        System.out.println(b);
        char c = (char) -1;
        System.out.println(c);
        byte d = (byte) 127;
        System.out.println(d);
        byte e = (byte) 128;
        System.out.println(e);
    }
}
