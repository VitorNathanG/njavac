public class NarrowFold {
    public static void main(String[] args) {
        int a = (char)-1;
        int b = (short)32768;
        int c = (byte)128;
        int d = (byte)300;
        int e = (char)65536;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
