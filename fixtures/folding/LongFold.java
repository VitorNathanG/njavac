public class LongFold {
    public static void main(String[] args) {
        long a = 2L * 3L;
        long b = 100L % 7L;
        long c = 1L + 2;
        long d = 1000000L * 1000000L;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
