public class TruncCastFold {
    public static void main(String[] args) {
        int f = (int)4000000000L;
        int g = (int)2.9;
        long h = (long)7;
        float k = (float)5;
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
        System.out.println(k);
    }
}
