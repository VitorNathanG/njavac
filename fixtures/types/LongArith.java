public class LongArith {
    public static void main(String[] args) {
        long a = 10L;
        long b = 3L;
        long add = a + b;
        long sub = a - b;
        long mul = a * b;
        long div = a / b;
        long rem = a % b;
        long neg = -a;
        System.out.println(add);
        System.out.println(sub);
        System.out.println(mul);
        System.out.println(div);
        System.out.println(rem);
        System.out.println(neg);
    }
}
