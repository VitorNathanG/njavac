public class FloatArith {
    public static void main(String[] args) {
        float x = 10.0f;
        float y = 3.0f;
        float add = x + y;
        float sub = x - y;
        float mul = x * y;
        float div = x / y;
        float rem = x % y;
        float neg = -x;
        System.out.println(add);
        System.out.println(sub);
        System.out.println(mul);
        System.out.println(div);
        System.out.println(rem);
        System.out.println(neg);
    }
}
