public class FloatLadder {
    public static void main(String[] args) {
        float a = 0.0f;
        float b = 1.0f;
        float c = 2.0f;
        float d = 3.5f;
        float e = 3.0f;
        float g = 1e9f;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(g);
    }
}
