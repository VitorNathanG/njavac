public class ShortBoundary {
    public static void main(String[] args) {
        short a = 5;
        short b = 6;
        short c = 127;
        short d = 128;
        short e = 32767;
        short f = -32768;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}
