public class DoubleArith {
    public static void main(String[] args) {
        double x = 10.0;
        double y = 3.0;
        double add = x + y;
        double sub = x - y;
        double mul = x * y;
        double div = x / y;
        double rem = x % y;
        double neg = -x;
        System.out.println(add);
        System.out.println(sub);
        System.out.println(mul);
        System.out.println(div);
        System.out.println(rem);
        System.out.println(neg);
    }
}
