public class DoubleLadder {
    public static void main(String[] args) {
        double a = 0.0;
        double b = 1.0;
        double c = 2.0;
        double d = 3.14;
        double e = 1e9;
        double f = 0.5;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}
