public class RtSlot13 {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        int c = 3;
        long d = 4L;
        int e = a + b;
        int f = b + c;
        long g = d + 1L;
        int h = e + f;
        int i = h + a;
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
        System.out.println(i);
    }
}
