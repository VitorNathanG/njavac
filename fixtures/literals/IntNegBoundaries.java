public class IntNegBoundaries {
    public static void main(String[] args) {
        int a = -1;
        int b = -128;
        int c = -129;
        int d = -32768;
        int e = -32769;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
