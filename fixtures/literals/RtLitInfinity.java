public class RtLitInfinity {
    public static void main(String[] args) {
        float pf = 1.0f / 0.0f;
        float nf = -1.0f / 0.0f;
        double pd = 1.0 / 0.0;
        double nd = -1.0 / 0.0;
        System.out.println(pf);
        System.out.println(nf);
        System.out.println(pd);
        System.out.println(nd);
    }
}
