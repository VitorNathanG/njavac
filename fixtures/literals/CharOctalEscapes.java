public class CharOctalEscapes {
    public static void main(String[] args) {
        int e0 = '\0';
        int e7 = '\7';
        int e77 = '\77';
        int e377 = '\377';
        int e101 = '\101';
        System.out.println(e0);
        System.out.println(e7);
        System.out.println(e77);
        System.out.println(e377);
        System.out.println(e101);
    }
}
