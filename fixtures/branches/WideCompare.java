public class WideCompare {
    public static void main(String[] args) {
        long l = 5L;
        float f = 1.5f;
        double d = 2.5;
        if (l > 3L) { System.out.println(1); }
        if (f < 2.0f) { System.out.println(2); }
        if (f >= 1.0f) { System.out.println(3); }
        if (d <= 9.0) { System.out.println(4); }
        if (d != 1.0) { System.out.println(5); }
    }
}
