public class ZeroCompare {
    public static void main(String[] args) {
        int a = 3;
        if (a > 0) { System.out.println(1); }
        if (a < 0) { System.out.println(2); }
        if (a == 0) { System.out.println(3); }
        if (a != 0) { System.out.println(4); }
        if (0 < a) { System.out.println(5); }
    }
}
