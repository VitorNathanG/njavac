public class RtPrn11 {
    public static void main(String[] args) {
        int i = 6;
        int k = 2;
        long j = 40L;
        System.out.println(i << k);
        System.out.println(i & k);
        System.out.println(j | 1L);
        System.out.println(i >> 1);
        System.out.println(~j);
        System.out.println(-i);
    }
}
