public class RtBit8 {
    public static void main(String[] args) {
        boolean p = true;
        boolean q = false;
        boolean r1 = p & q;
        boolean r2 = p | q;
        boolean r3 = p ^ q;
        System.out.println(r1);
        System.out.println(r2);
        System.out.println(r3);
    }
}
