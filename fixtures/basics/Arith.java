public class Arith {
    public static void main(String[] args) {
        int x = 2 + 3 * 4 - (5 - 1);
        int y = 100 / 7;
        int z = 100 % 7;
        int w = -x + y * -z;
        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
        System.out.println(w);
    }
}
