public class RtCmp13 {
    public static void main(String[] args) {
        char c = 'z';
        c++;
        --c;
        byte b = 5;
        b++;
        --b;
        short s = 200;
        s++;
        --s;
        System.out.println(c);
        System.out.println(b);
        System.out.println(s);
    }
}
