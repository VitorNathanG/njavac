public class RtCmp12 {
    public static void main(String[] args) {
        int i = 0;
        i++;
        ++i;
        i--;
        --i;
        long l = 100L;
        l++;
        --l;
        float f = 2.5f;
        f++;
        --f;
        double d = 9.0;
        d++;
        --d;
        System.out.println(i);
        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
    }
}
